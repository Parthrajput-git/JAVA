import java.util.*;
public class Bit{
    public static void main(String args[]){
        System.out.print("AND oparater  :");
        System.out.println(5 & 6);
        System.out.print("OR oparater  :");
        System.out.println(5|6);
        System.out.print("XOR oparater  :");
        System.out.println(5^6);
        System.out.print("Ons's(Tild) oparater  :");
        System.out.println(~5);
        System.out.print("Left shift oparater  :");
        System.out.println(5<<2);
        System.out.print("Right shift oparater :");
        System.out.println(6>>1);
}
}